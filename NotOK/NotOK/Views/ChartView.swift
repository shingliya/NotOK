import SwiftUI
import Charts

struct CandleStick: Codable, Identifiable {
    var id: String {
        return timestamp
    }
    let timestamp: String
    //    let open: String
    //    let high: String
    //    let low: String
    let close: String
    //    let volume: String
    //    let volumeCurrency: String
    //    let volumeCurrencyQuote: String
    //    let confirm: String
}


struct ChartView: View {
    let candleSticks = sampleData.map { item -> CandleStick in
        return CandleStick(
            timestamp: item[0],
            //            open: item[1],
            //            high: item[2],
            //            low: item[3],
            close: item[4]
            //            volume: item[5],
            //            volumeCurrency: item[6],
            //            volumeCurrencyQuote: item[7],
            //            confirm: item[8]
        )
    }
    
    var body: some View {
        let sortedCandleSticks = candleSticks
            .sorted { (candle1, candle2) in
                let timestamp1 = Double(candle1.timestamp) ?? 0
                let timestamp2 = Double(candle2.timestamp) ?? 0
                return timestamp1 < timestamp2
            }
        
        let closeValues = sortedCandleSticks.compactMap { Double($0.close) }
        let minClose = (closeValues.min() ?? 0)
        let minXBoundary = minClose * 0.985
        let maxClose = (closeValues.max() ?? 0)
        let maxXBoundary = maxClose * 1.015
        
        let timestampValues = sortedCandleSticks.compactMap { Double($0.timestamp) }
        let beginTime = timestampValues.min() ?? 0
        let endTime = timestampValues.max() ?? 0
        
        VStack {
            Chart(sortedCandleSticks, id: \.timestamp) { candleStick in
                LineMark(
                    x: .value("Date", Double(candleStick.id) ?? 0),
                    y: .value("Open Price", Double(candleStick.close) ?? 0)
                )
                .foregroundStyle(.blue)
                .shadow(color: .blue, radius: 5, x: 0, y: 0)
                AreaMark(
                    x: .value("Date", Double(candleStick.id) ?? 0),
                    y: .value("Open Price", Double(candleStick.close) ?? 0)
                )
                .foregroundStyle(.blue.opacity(0.3))
            }
            .chartXScale(domain: beginTime...endTime)
            .chartYScale(domain: minXBoundary...maxXBoundary)
            .chartXAxis(.hidden)
            .chartYAxis(.hidden)
            .aspectRatio(2.3, contentMode: .fit)
            .clipped()
        }
        .overlay(
            GeometryReader { geo in
                if let minCloseIndex = closeValues.firstIndex(of: minClose){
                    Text("$\(minClose, specifier: "%.2f")")
                        .font(.caption)
                        .foregroundColor(Color.gray)
                        .position(x: geo.size.width * (Double(minCloseIndex) / 288), y: geo.size.height * 0.9)
                }
                
                if let maxCloseIndex = closeValues.firstIndex(of: maxClose){
                    Text("$\(maxClose, specifier: "%.2f")")
                        .font(.caption)
                        .foregroundColor(Color.gray)
                        .position(x: geo.size.width * (Double(maxCloseIndex) / 288), y: geo.size.height * 0.15)
                }
            }
                .padding(.horizontal)
        )
    }
}

